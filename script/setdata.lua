--[[
	伤害公式
		虽然人物属性影响伤害还未定,但我还是把这段时间的一些想法整理一下
		
		首先是核心公式 hurt X atk/(atk+def)
		最初的设计，atk即人物的总攻击力，所有武功的攻击力之和，def即除了外功外所有武功的防御力之和，
			当然atk和def还需要经过人物属性的修正，这里忽略了 
			hurt使用atk的值
			简单的写作atk x atk/(atk+def)
		现在比较倾向于hurt X atk/(atk+def)
			hurt:外功的攻击力,还需要经过人物属性的修正
			atk:除外功外,所有武功的攻击力之和,再经内力值修正
			def:除外功外,所有武功的防御力之和,再经内力值修正
				内力值修正取(当前内力X2+内力最大值)/3
		两个公式的比较,第一个公式是很明显的对atk有优势,
			第二个公式则把hurt和atk分开,即伤害最主要的部分是选择使用的武功以及人物的属性
			内功等其他武功对伤害值起次要作用
			
		次要公式
			核心公式的结果是很难出现很小的值得,亦即一些低级武功使用后,依然会对高级的人产生一定程度的伤害
			次要公式的作用即 一定程度地消除这些伤害
			次要公式是线性的,属性值变动后,可以明显的看出变化
			数值比较低时,次要公式作用突出,数值比较高时,次要公式基本可以忽略
			公式为hurt+(atk-def)/a+(攻方装备攻击-守方装备防御)/b
				装备的作用,其攻防数值作用是次要的,主要作用是装备附带的特效
				
		最终公式
			hurt=武功威力X武功等级
			hurt=人物属性修正(hurt,人物属性)
			atk=内功轻功特技 攻击之和
			def=内功轻功特技 防御之和
			内力修正=(当前内力X2+内力最大值)/3
			atk=修正(atk,内力修正)
			def=修正(def,内力修正)
			--或者修正函数为 atk*min((0.5+当前内力/最大内力),1)即在0.5+当前内力/最大内力和1之间取较小值
			--即内力为最大内力一半以上时，发挥100%能力，内力继续降低则降低能力，最低降为50%
			--[具体数值我是随意设置的，需再议]，大体觉得开始降低能力的门槛低了点，内力为0时的能力值高了点
			hurt=hurt*atk/(atk+def)+(atk-def)/a+(攻方装备攻击-守方装备防御)/b
			hurt=距离修正(hurt)，最多会削弱为2/3(考虑若攻击方为敌人则忽略这一步)
			在这之后,是特效对伤害进行修正
			先进行伤害百分比修正,接着是额外伤害修正
			然后附加一个随机数
]]--

















