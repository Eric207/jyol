--[[
	集气
		考虑每5回合多行动一次，我感觉是比较合适的一个程度，幅度不是太高，但又能让人明显的感到速度的差异
		考虑每10回合多行动一次，这个就不明显了，只有战斗回合数比较长才能体现出来
		简单起见可以考虑默认值为20
			若较明显的感觉到速度差异，则等比数列，1.25：[20，25，31，39，49，61]
			若不明显，则等比数列，1.111：[20，22，24，27，30，34，38]
		轻功和内功有一定的影响，可以为正值，也可以为负
		轻功的效果应该比内功更多一些
		以轻功为例，增加幅度应该大体按照轻功的阶级来，考虑每一阶级能让人较明显的感觉到速度的差异
			考虑没轻功的情况下，+0
			低级轻功如XX身法之流，+5，上下略微波动
			中级轻功如梯云纵金燕功，+11，上下略微波动
			高级轻功如凌波神行，+19，上下略微波动
		以内功为例，
			这个就不必按照内功的阶级来区分加集气值，甚至某些内功减集气
		外功
			外功的影响体现在使用该武功后,人物的集气值
			默认情况下,使用武功后集气归0
			可以把一些明显速度很快的武功,比如'夺命连环三仙剑'设置成使用该武功后,集气为100,相当于速度快了1/10
			把一些明显需要较大气力才能使用的武功,如'一拍两散'设置成使用后,集气为-200,当当与速度慢了1/5
		考虑武功等级的影响，打算武功本身占3/10，武功等级影响占7/10，公式为 集气值X(0.3+等级/15)
